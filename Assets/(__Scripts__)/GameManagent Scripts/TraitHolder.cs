using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class TraitHolder : MonoBehaviour
{
    // Unity GameObjects: //////////////////////////////////////////////////////
    // Unity Components: ///////////////////////////////////////////////////////
    // C# Properties: //////////////////////////////////////////////////////////
    public int Count { get { return traits.Length; } }

    // C# Fields: //////////////////////////////////////////////////////////////
    [SerializeField] Trait[] traits;

    // Unity Main Events: //////////////////////////////////////////////////////
    // Unity Other Events: /////////////////////////////////////////////////////
    // C# Public Methods: //////////////////////////////////////////////////////
    public bool Contain(Trait trait)
    {
        for(int i = 0; i< traits.Length; i++)
        {
            if (traits[i] == trait) return true;
        }
        return false;
    }

    // C# Private Methods: /////////////////////////////////////////////////////

} // end of class

// //////////////////////////////////////////////////////////////////////////////////////////////////////////////////
public enum Trait : byte
{
    None,
    CanMove,
    Wirable,
    HaveBorder,
    Input,
    Output,
    VirutalButton,
    HaveContextMenu,
}